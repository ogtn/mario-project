#ifndef KEYSTATE_H
#define KEYSTATE_H


/******************************************************************************/
/*=================================[INCLUDES]=================================*/
/******************************************************************************/

#include "constantes.h"


/******************************************************************************/
/*================================[STRUCTURES]================================*/
/******************************************************************************/

/*
_actuel: tableau de l'etat des touches actuellement
_precedent: tableau d'etat des touches à l'appel precedent de la fonction */
typedef struct keystate
{
	char actuel[NB_TOUCHES];
	char precedent[NB_TOUCHES];
} keystate;


/******************************************************************************/
/*================================[PROTOTYPES]================================*/
/******************************************************************************/

/* Alloue de la mémoire pour tableau d'etat des touches */
keystate* new_keystate(void);

/* initialise les champs de keystate */
keystate* init_keystate(keystate *k);

/* liberation de la structure */
keystate *free_keystate(keystate *k);

/* Gestion des evenements clavier */
void maj_keystate(keystate *keystate, int *continuer);


#endif
